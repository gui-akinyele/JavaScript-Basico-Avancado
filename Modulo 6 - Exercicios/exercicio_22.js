//* Faça uma função que receba um número negativo e retorne um número positivo;

//! Utilize a função Math.Labs;

function numeroPos(numNegativo) {
    return Math.abs(numNegativo);
}

console.log(numeroPos(-17));
console.log(numeroPos(-25));
console.log(numeroPos(13));
console.log(numeroPos(-19.55));
