// Estruturas de repetição: WHILE

// Fará uma ação, até que uma condição seja atiginda;
// Precisamos difinir um fim para o loop, para não termos o problema de loop infinito;

let x = 10;

while(x > 0) {
    console.log(x);
    x = x - 1;
}


let y = 10;

while(y > 0) {
    console.log("O Y vale: " + y);
    y = y - 1;
}

let z = 0;

while(z <= 10) {
    console.log(z);
    z = z + 1;
}





