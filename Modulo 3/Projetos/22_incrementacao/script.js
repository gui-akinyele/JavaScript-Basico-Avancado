// Incrementação

// Podemos incrementar uma variável de forma mais fácil: x += 1;

// Ou x++;

for(let numero = 0; numero < 10; numero += 1) {
    console.log("O número é: " + numero);
}

let  numero = 5;

while(numero < 50) {
    console.log(numero);

    numero += 10;
}

for(let i = 0; i < 10; i++) {
    console.log("O i é: " + i);
}

for(let i = 10; i > 0; i--) {
    console.log("O i é: " + i);
}