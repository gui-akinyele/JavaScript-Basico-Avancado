//O que são variáveis?

// Salvamos valores em variáveis (isso é um statement);

 let laranjas = 5;

// E depois podemos criar expressões com os valores salvos;
console.log(laranjas * laranjas);
console.log(`Tem ${laranjas} laranjas na sexta`);


// Como salvar valores na memória
laranjas = laranjas + 1;
console.log(laranjas);

// Um let pode definir várias variáveis também;
let quadrado = 4, triangulo = 3, retangulo = 4;
console.log(quadrado * (triangulo + retangulo));

let nome = "Gui";

console.log(nome);
console.log(`O meu nome é ${nome}`);

nome = 'Josias';

console.log(nome);

laranjas = 345;

console.log(laranjas);

laranjas = "laranja";

console.log(laranjas);

let um = 1, dois = 2, tres = 3;

console.log(um + dois + tres);




