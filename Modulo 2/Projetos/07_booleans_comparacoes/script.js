// BOOLEANS: COMPARAÇÕES

// Maior que >
// Menor que <
// Maior ou igual >=
// Menor ou igual <=
// Igual ==
// Diferente !=
// Idêntico === Mesmo valor e mesmo tipo de variável

console.log(1 > 2);
console.log(5 < 10);
console.log(3 >= 3);
console.log(5 <= 4);
console.log(5 == 4);
console.log('Gui' != 'Gui');
console.log(3 === '3');
console.log(4 === 4);


