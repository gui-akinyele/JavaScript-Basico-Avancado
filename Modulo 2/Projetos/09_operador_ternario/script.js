// BOOLEANS: OPERADORES TERNÁRIO

// Faz comparativo em apenas uma linha de código;

console.log(true ?1:2);
console.log(false ?'falso':'verdadeiro');

// Não é utilizado e pode deixar o código complicado de ler;

console.log(5 > 2 ? 'É sim' : 'É não');
console.log(false ? 5:4);
console.log('Gui' == 'Gui' ? 'Olá, Gui' : 'Não é o Gui');