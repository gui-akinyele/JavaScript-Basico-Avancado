// STRINGS AVANÇADO

// A \ pode dar um 'escape' na String, isso permite 'efeitos especiais';
// Por exemplo: \n pula uma linha;

console.log("Essa é uma String \n de duas linhas");

// Para inserir uma ' ou " devemos iniciar com a aspa inversa que desejamos inserir;

console.log("Meu nome é 'Josias'");
console.log('Nome dele é "Osvaldo"');

// O template literals serve para computar valores também, ex:

console.log(`A soma de 2 + 2 é ${2+2}`);

//Concatenação é um processo de "somar" Strings, veja:

console.log("salada " + "de " + "fruta");
